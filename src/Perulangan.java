public class Perulangan {
    public static void main(String[] args) {
        // FOR LOOP
        for (int i = 1; i <= 10; i++) {
            System.out.println("Perulangan For ke-"+i);
        }
        
        // WHILE LOOP
        int j = 1;
        while (j <= 10) {
            System.out.println("Perulangan While ke-"+j);
            j++;
        }
        
        // DO WHILE LOOP
        int k = 1;
        do {
            System.out.println("Perulangan Do While ke-"+k);
            k++;
        } while (k <= 10);

    }
}
