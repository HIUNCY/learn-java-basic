public class OperasiPerbandingan {
    public static void main(String[] args) {
        int val1 = 100;
        int val2 = 100;
        System.out.println(val1>val2);
        System.out.println(val1>=val2);
        System.out.println(val1<val2);
        System.out.println(val1<=val2);
        System.out.println(val1==val2);
        System.out.println(val1!=val2);
    }
}
