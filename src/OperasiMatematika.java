public class OperasiMatematika {
    public static void main(String[] args) {
        int a = 5;
        int b = 21;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        a += 10;
        b -= 6;
        System.out.println(a);
        System.out.println(b);
        
        a++;
        b--;
        System.out.println(a);
        System.out.println(b);

    }
}
